import java.util.*;
class main
{
	public static void main (String[] args) throws java.lang.Exception
	{
	    Scanner sc=new Scanner(System.in);
	    int t=sc.nextInt();
	    for(int i=0;i<t;i++){
	        int c=sc.nextInt();
	        int u=sc.nextInt();
	        int chap=sc.nextInt();
	        System.out.println(c*u*chap);
	    }
	}
}

